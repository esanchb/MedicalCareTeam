    void acceder (String usuario, String pass)
    {
        conn mysql = new conn();
        Connection cn = mysql.Conectar();
        
        String cap="";
        //String sql="SELECT * FROM usuario WHERE nick=`"+usuario+"` && pass=`"+pass+"`";
        String sql = ("SELECT * FROM `usuario`  WHERE `nick` LIKE '%" + usuario + "%'"); 
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next())
            {
                cap=rs.getString("tipo");
            }
            if(cap.equals("admin"))
            {
                  this.setVisible(false);
                  JOptionPane.showMessageDialog(null, "Bienvenido");
                  ingreso ventana = new ingreso();
                  ventana.setVisible(true);
                  //ingreso.txtAdmin.setText(usuario);
                /*if (tbt_ingreso.isSelected())
                {
                  //this.setVisible(false);
                  JOptionPane.showMessageDialog(null, "Bienvenido");
                  ingreso ventana = new ingreso();
                  ventana.setVisible(true);
                  //ingreso.txtAdmin.setText(usuario);
                }
                
                if (btConsulta.isSelected())
                {
                  //this.setVisible(false);
                  JOptionPane.showMessageDialog(null, "Bienvenido");
                  Consulta opc = new Consulta();
                  opc.setVisible(true);
                  //Consulta.txtAdmin.setText(usuario);
                }  
            }*/
            }
            if(cap.equals("doc"))
            {
                  this.setVisible(false);
                  JOptionPane.showMessageDialog(null, "Bienvenido");
                  Consulta ventana = new Consulta();
                  ventana.setVisible(true);
            }
            
           if((!cap.equals("admin"))&&(!cap.equals("doc")))
            {
                JOptionPane.showMessageDialog(null, "Contraseña o usuario incorrectos");
            }
        } catch (SQLException ex) {
            
        }
        
    }
